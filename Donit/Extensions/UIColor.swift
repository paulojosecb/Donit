import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var greenyBlue: UIColor {
        return UIColor(red: 62.0 / 255.0, green: 163.0 / 255.0, blue: 178.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightishBlue: UIColor {
        return UIColor(red: 72.0 / 255.0, green: 94.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var black: UIColor {
        return UIColor(white: 0.0, alpha: 1.0)
    }
    
    @nonobjc class var white: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var black70: UIColor {
        return UIColor(white: 0.0, alpha: 0.7)
    }
    
    @nonobjc class var black20: UIColor {
        return UIColor(white: 0.0, alpha: 0.2)
    }
    
    @nonobjc class var pale: UIColor {
        return UIColor(red: 1.0, green: 235.0 / 255.0, blue: 202.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightSalmon: UIColor {
        return UIColor(red: 239.0 / 255.0, green: 163.0 / 255.0, blue: 156.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var periwinkleBlue: UIColor {
        return UIColor(red: 133.0 / 255.0, green: 163.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var iris: UIColor {
        return UIColor(red: 87.0 / 255.0, green: 113.0 / 255.0, blue: 190.0 / 255.0, alpha: 1.0)
    }
    
}
